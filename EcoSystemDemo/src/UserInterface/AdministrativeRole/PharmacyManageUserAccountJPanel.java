    private void createActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createActionPerformed
         String userName = uname.getText();
        String password = upass.getText();
        Organization organization = (Organization) emptypecombo.getSelectedItem();
        Employee employee = (Employee) empnamecombo.getSelectedItem();
        Role role = (Role) rolecombo.getSelectedItem();

        if(userName.isEmpty()||password.isEmpty()){
            JOptionPane.showMessageDialog(null, "Username/ Password field empty!","Warning",JOptionPane.WARNING_MESSAGE);
            return;
            
        }
        
            for(UserAccount ua:organization.getUserAccountDirectory().getUserAccountList()){
                if(ua.getUsername().equals(userName)){
                    JOptionPane.showMessageDialog(null, "User account already exist!","Warning",JOptionPane.WARNING_MESSAGE);
                    return;
                }
            }
          
                organization.getUserAccountDirectory().createUserAccount(userName, password, employee, role);
                popData();
                DB4OUtil.getInstance().storeSystem(system);
                 JOptionPane.showMessageDialog(null, "User created successfully","Success",JOptionPane.PLAIN_MESSAGE);
                uname.setText("");
                upass.setText("");
                
        
    }//GEN-LAST:event_createActionPerformed

