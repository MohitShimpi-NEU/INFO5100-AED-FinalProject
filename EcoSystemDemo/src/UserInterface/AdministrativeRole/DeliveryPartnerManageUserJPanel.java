    public void populateEmployeeComboBox(Organization organization){
        empname.removeAllItems();
        
        for (Employee employee : organization.getEmployeeDirectory().getEmployeeList()){
            empname.addItem(employee);
        }
    }
    
    private void populateRoleComboBox(Organization organization){
        rolecombo.removeAllItems();
        for (Role role : organization.getSupportedRole()){
            rolecombo.addItem(role);
        }
    }

    public void popData() {

        DefaultTableModel model = (DefaultTableModel) emptab.getModel();

        model.setRowCount(0);

        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
            for (UserAccount useraccount : organization.getUserAccountDirectory().getUserAccountList()) {
                Object row[] = new Object[3];
                row[0] = organization;
                row[1] = useraccount.getUsername();
                row[2] = useraccount.getPassword();
                ((DefaultTableModel) emptab.getModel()).addRow(row);
            }
        }
    }
