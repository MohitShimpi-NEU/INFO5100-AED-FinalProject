    private void createbtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createbtActionPerformed
        String userName = nametxt.getText();
        String password = passtxt.getText();
        Organization organization = (Organization) emptype.getSelectedItem();
        Employee emp = (Employee) empname.getSelectedItem();
        Role r = (Role) rolecombo.getSelectedItem();

        if(userName.isEmpty()||password.isEmpty()){
            JOptionPane.showMessageDialog(null, "Username/ Password field empty!","Warning",JOptionPane.WARNING_MESSAGE);
            return;
        }
        else{
            for(UserAccount ua:organization.getUserAccountDirectory().getUserAccountList()){
                if(ua.getUsername().equals(userName)){
                    JOptionPane.showMessageDialog(null, "User already exist!","Warning",JOptionPane.WARNING_MESSAGE);
                    return;
                }
            }
           
                organization.getUserAccountDirectory().createUserAccount(userName, password, emp, r);
                popData();
                JOptionPane.showMessageDialog(null, "User created successfully","Success",JOptionPane.PLAIN_MESSAGE);
                    
                nametxt.setText("");
                passtxt.setText("");
                
        }
    }//GEN-LAST:event_createbtActionPerformed

