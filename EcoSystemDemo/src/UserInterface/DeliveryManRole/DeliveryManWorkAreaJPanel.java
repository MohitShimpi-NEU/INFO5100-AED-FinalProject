    private void takeorderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_takeorderActionPerformed
        // TODO add your handling code here:
        int selectedrow = ordertable.getSelectedRow();

        if(selectedrow <0)
        {
             JOptionPane.showMessageDialog(null,"Please select an order!","Warning",JOptionPane.WARNING_MESSAGE);
             return;
        }
        
       
        Order  order = (Order)ordertable.getValueAt(selectedrow,0);
            
            order.setStatus("Deliverying");
            order.setDName(userAccount.getUsername());
            
            
            JOptionPane.showMessageDialog(null, "Order assigned successfully","Warning",JOptionPane.WARNING_MESSAGE);
            populateOrderTable();
            populateYourOrderTable();
            DB4OUtil.getInstance().storeSystem(system);
    }//GEN-LAST:event_takeorderActionPerformed

