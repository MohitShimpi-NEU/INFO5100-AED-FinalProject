    private void populateOrderTable(){
        DefaultTableModel model = (DefaultTableModel) ordertable.getModel();
        model.setRowCount(0);
        Network network =null;
        for(Network n1 :system.getNetworkList()){
            for(Enterprise en: n1.getEnterpriseDirectory().getEnterpriseList()){
                if(en.getName().equals(enterprise.getName())){
                    network = n1;
                    break;
                }
            }
        }
       
        for (Order o : system.getOrderDirectory().getOrderList()){
            if(o.getStatus().equals("packed")&& network.getName().equals(o.getNetwork().getName())){
                Object[] row = new Object[4];
                row[0] = o;
                
                row[1] = o.getRName();
                row[2] = o.getAdd();
                row[3] = o.getStatus();
                model.addRow(row);
            }
            
        }
    }
     private void populateYourOrderTable(){
        DefaultTableModel model = (DefaultTableModel) orderinfotable.getModel();
        model.setRowCount(0);
        for (Order o : system.getOrderDirectory().getOrderList()){
            if(o.getStatus().equals("Deliverying")&&o.getDName().equals(userAccount.getUsername())){
                Object[] row = new Object[4];
                row[0] = o;
                
                row[1] = o.getRName();
                row[2] = o.getAdd();
                row[3] = o.getStatus();
                model.addRow(row);
            }
            
        }
    }
    

